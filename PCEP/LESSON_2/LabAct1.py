num1 = int(input("Enter thr First number : "))
num2 = int(input("Enter thr Second number : "))

if num1 > num2:
    result = num1 - num2
    print("The difference between the numbers is : ", result)   
elif num1 < num2 :
    result = num1 + num2   
    print("The Sum of the numbers is : ", result)   
else :
    result = num1 * num2    
    print("The Product of the numbers is : ", result)    